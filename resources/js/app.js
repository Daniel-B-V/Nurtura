import './bootstrap';

import Alpine from 'alpinejs';

window.Alpine = Alpine;

Alpine.start();

// Realtime Dashboard Updates
if (window.Echo) {
    // Listen for dashboard channel events
    window.Echo.channel('dashboard')
        .listen('ChildRegistered', (e) => {
            console.log('New child registered:', e);
            showRealtimeNotification('New Child Registered', e.message, 'success');
            // Optionally reload stats or update the UI dynamically
            setTimeout(() => window.location.reload(), 2000);
        })
        .listen('DonationReceived', (e) => {
            console.log('New donation received:', e);
            showRealtimeNotification('New Donation', e.message, 'success');
            setTimeout(() => window.location.reload(), 2000);
        })
        .listen('InventoryLowStock', (e) => {
            console.log('Inventory low stock:', e);
            showRealtimeNotification('Low Stock Alert', e.message, 'warning');
        });

    // Listen for user-specific notifications
    const userId = document.querySelector('meta[name="user-id"]')?.content;
    if (userId) {
        window.Echo.private(`user.${userId}`)
            .listen('NotificationSent', (e) => {
                console.log('New notification:', e);
                showRealtimeNotification(e.title, e.message, e.type);
            });
    }
}

// Show realtime notification toast
function showRealtimeNotification(title, message, type = 'info') {
    const notification = document.createElement('div');
    notification.className = `fixed top-4 right-4 z-50 p-4 rounded-lg shadow-lg max-w-sm animate-slide-in ${getNotificationColor(type)}`;
    notification.innerHTML = `
        <div class="flex items-start gap-3">
            <div class="flex-shrink-0">
                ${getNotificationIcon(type)}
            </div>
            <div class="flex-1">
                <h4 class="font-bold text-sm mb-1">${title}</h4>
                <p class="text-sm">${message}</p>
            </div>
            <button onclick="this.parentElement.parentElement.remove()" class="flex-shrink-0 text-gray-500 hover:text-gray-700">
                <svg class="w-5 h-5" fill="currentColor" viewBox="0 0 20 20">
                    <path fill-rule="evenodd" d="M4.293 4.293a1 1 0 011.414 0L10 8.586l4.293-4.293a1 1 0 111.414 1.414L11.414 10l4.293 4.293a1 1 0 01-1.414 1.414L10 11.414l-4.293 4.293a1 1 0 01-1.414-1.414L8.586 10 4.293 5.707a1 1 0 010-1.414z" clip-rule="evenodd"/>
                </svg>
            </button>
        </div>
    `;
    document.body.appendChild(notification);

    // Auto remove after 5 seconds
    setTimeout(() => {
        notification.classList.add('animate-slide-out');
        setTimeout(() => notification.remove(), 300);
    }, 5000);
}

function getNotificationColor(type) {
    const colors = {
        success: 'bg-green-50 border-l-4 border-green-500 text-green-900',
        warning: 'bg-yellow-50 border-l-4 border-yellow-500 text-yellow-900',
        error: 'bg-red-50 border-l-4 border-red-500 text-red-900',
        info: 'bg-blue-50 border-l-4 border-blue-500 text-blue-900',
    };
    return colors[type] || colors.info;
}

